SELECT SUM(Стипендия) AS Суммарная_стипендия, Код_факультета
FROM Студент
GROUP BY Код_факультета;

SELECT *,
	ROUND((Стипендия * 100.0 / (SELECT SUM(Стипендия) FROM Студент WHERE Код_факультета = "outer".Код_факультета)), 2) AS Доля,
	(SELECT SUM(Стипендия) FROM Студент WHERE Код_факультета = "outer".Код_факультета) AS Стипендия_факультета
FROM Студент AS "outer";